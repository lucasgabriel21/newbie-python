# Métodos de listas:

# .append(): insere elementos no final da lista

# .extend(): estende a lista a com os elementos de uma lista passada como parâmetro

# .pop(): remove e retorna o item da posição determinada, por padrão é o último se () fica vazio

# .insert(): insere novo item  na posição indicada

# .sort(): ordena a lista

# .reverse(): ordena a lista em ordem inversa

# .index(): retorna a posição da primeira ocorrência do item indicado

# .count(): retorna o número de ocorrências do item

# .remove(): remove a primeira ocorrência do item