# .upper(): passa para caixa alta (maiúscula)

# .lower(): passa para caixa baixa (minúscula)

# .isupper(): True se está em caixa alta

# .islower():True se está em caixa baixa

# .capitalize(): coloca a primeira letra em maiúscula

# .title(): Coloca a primeira letra de cada palavra em maiúscula

# .replace(x,y): troca as ocorrências de x por y

# .count(x): conta a ocorrência de x

# .startswith(x): verifica se uma string inicia com a string x

# .endswith(x) : verifica se uma string termina com a string x

# .find(x): pesquisa se a string x está dentro de outra e obtém a posição da primeira ocorrência ou -1, caso não encontre

# .rfind(x): idem ao find, mas inicia a pesquisa da direita para a esquerda

# .strip(): remove espaços em branco do início e fim da string

# .lstrip() e rstrip(): removem espaços em branco apenas à esquerda (lstrip) ou à direita (rstrip)

# .isalnum(): retorna True se a string não estiver vazia e contiver apenas letras ou números. Se houver espaço
# ou caracteres especiais, retornará False

# .isalpha(): retorna True se a string não estiver vazia e contiver apenas letras

# .isdigit(): retorna True se a string não estiver vazia e contiver apenas números